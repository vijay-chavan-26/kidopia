								<b>1.	Show and tell.</b><br> Teach children right from wrong with calm words and actions. Model behaviours you would like to see in your children.<br>
<b>2.	Set limits.</b><br> Have clear and consistent rules your children can follow. Be sure to explain these rules in age-appropriate terms they can understand.<br>
<b>3.	Give consequences.</b><br> Calmly and firmly explain the consequences if they don't behave. For example, tell her that if she does not pick up her toys, you will put them away for the rest of the day. Be prepared to follow through right away. Don't give in by giving them back after a few minutes. But remember, never take away something your child truly needs, such as a meal.<br>
<b>4.	Hear them out.</b><br> Listening is important. Let your child finish the story before helping solve the problem. Watch for times when misbehavior has a pattern, like if your child is feeling jealous. Talk with your child about this rather than just giving consequences.
<br><b>5.	Give them your attention. </b><br>The most powerful tool for effective discipline is attention—to reinforce good behaviors and discourage others. Remember, all children want their parent's attention.
<br><b>6.	Catch them being good.</b><br> Children need to know when they do something bad--and when they do something good. Notice good behavior and point it out, praising success and good tries. Be specific (for example, "Wow, you did a good job putting that toy away!").
